import { Controller } from "@hotwired/stimulus"

export default class extends Controller {
  static targets = ["border", "pourcentage"]
  static values = {number: String}

  connect() {
    if (this.numberValue.substr(0, 1) === "-") {
      this.borderTarget.classList.add("card-company-little-red")
      this.pourcentageTarget.classList.add("stock-status-red")

      this.borderTarget.classList.remove("card-company-little-green")
      this.pourcentageTarget.classList.remove("stock-status-green")
    } else {
      this.borderTarget.classList.remove("card-company-little-red")
      this.pourcentageTarget.classList.remove("stock-status-red")

      this.borderTarget.classList.add("advice-green")
      this.pourcentageTarget.classList.add("stock-status-green")
    }
  }


}
